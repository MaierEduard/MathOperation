package org.fasttrackit;

public class Calc {


    public  double numbersForDecrease( double number1, double number2){
        double decreaseResult = number1-number2;
        return decreaseResult;

    }

    public double numbersForDivision (double number1, double number2){
        double divisionResult = number1/number2;
        return divisionResult;
    }


public double numbersForMultiplication(double number1, double number2){

    double multiplicationResult = number1*number2;
    return multiplicationResult;
}


public double numbersForSum(double number1, double number2){

    double sumResult = number1+number2;
    return sumResult;





}

}
